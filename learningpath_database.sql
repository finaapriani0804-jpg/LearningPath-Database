FROM SISWA
ORDER BY TGL_LAHIR ASC;

-- 4. TOP-N (Oracle: ROWNUM)
SELECT NAMA_KURSUS, BIAYA_PERBULAN
FROM (
    SELECT NAMA_KURSUS, BIAYA_PERBULAN
    FROM KURSUS
    ORDER BY BIAYA_PERBULAN DESC
)
WHERE ROWNUM <= 3;

-- 5. SUBSTITUTION VARIABLE
SELECT * FROM SISWA
WHERE NAMA_SISWA = :KETIK_NAMA_LENGKAP_SISWA;

-- 6. JOIN

-- 2 Tabel
SELECT S.NAMA_SISWA,
       DF.ID_KURSUS
FROM SISWA S
JOIN PENDAFTARAN DF ON S.ID_SISWA = DF.ID_SISWA;

-- 3 Tabel
SELECT S.NAMA_SISWA      AS "Siswa",
       K.NAMA_KURSUS     AS "Kursus",
       P.NAMA_PENGAJAR   AS "Pengajar"
FROM SISWA S
JOIN PENDAFTARAN DF ON S.ID_SISWA   = DF.ID_SISWA
JOIN KURSUS K       ON DF.ID_KURSUS = K.ID_KURSUS
JOIN PENGAJAR P     ON K.ID_PENGAJAR = P.ID_PENGAJAR;

-- Non-Equi Join (CASE)
SELECT K.NAMA_KURSUS,
       K.BIAYA_PERBULAN,
       CASE
           WHEN K.BIAYA_PERBULAN < 300000                        THEN 'Murah'
           WHEN K.BIAYA_PERBULAN BETWEEN 300000 AND 350000       THEN 'Sedang'
           ELSE 'Mahal'
       END AS KATEGORI_HARGA
FROM KURSUS K
ORDER BY K.BIAYA_PERBULAN;

-- Self Join
SELECT P.NAMA_PENGAJAR  AS Pengajar,
       M.NAMA_PENGAJAR  AS Mentor
FROM PENGAJAR P
JOIN PENGAJAR M ON P.ID_MENTOR = M.ID_PENGAJAR;

-- Left Outer Join
SELECT P.NAMA_PENGAJAR,
       K.NAMA_KURSUS
FROM PENGAJAR P
LEFT OUTER JOIN KURSUS K ON P.ID_PENGAJAR = K.ID_PENGAJAR
ORDER BY P.NAMA_PENGAJAR;

-- Right Outer Join
SELECT K.NAMA_KURSUS,
       DF.ID_PENDAFTARAN
FROM PENDAFTARAN DF
RIGHT OUTER JOIN KURSUS K ON DF.ID_KURSUS = K.ID_KURSUS
ORDER BY K.NAMA_KURSUS;

-- Full Outer Join
SELECT P.NAMA_PENGAJAR,
       K.NAMA_KURSUS
FROM PENGAJAR P
FULL OUTER JOIN KURSUS K ON P.ID_PENGAJAR = K.ID_PENGAJAR
ORDER BY P.NAMA_PENGAJAR;

-- Cross Join
